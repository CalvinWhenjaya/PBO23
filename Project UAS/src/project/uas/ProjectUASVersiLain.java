    private void btncekdiskonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncekdiskonActionPerformed
        // TODO add your handling code here:
        double hargasatuan,diskon;
        int jumlah;
        
        jumlah = Integer.parseInt(txtjumlah.getText());
        hargasatuan = Double.parseDouble(txthargasatuan.getText());
        
        if (jumlah <5){
            diskon = 0;
        } else if (jumlah >5 && jumlah == 20){
            diskon = hargasatuan * 0.1;
        } else if (jumlah >20 && jumlah == 50){
            diskon = hargasatuan * 0.2;
        } else {
            diskon = hargasatuan * 0.3;
        }
        
        txtdiskon.setText(""+diskon);
    }//GEN-LAST:event_btncekdiskonActionPerformed

    private void btncektotalbayarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncektotalbayarActionPerformed
        // TODO add your handling code here:
        double hargasatuan,diskon,totalharga;
        int jumlah;
        
        hargasatuan = Double.parseDouble(txthargasatuan.getText());
        jumlah = Integer.parseInt(txtjumlah.getText());
        diskon = Double.parseDouble(txtdiskon.getText());
        
        totalharga = (hargasatuan * jumlah) - diskon;
        txthargatotal.setText(""+totalharga);
    }//GEN-LAST:event_btncektotalbayarActionPerformed

