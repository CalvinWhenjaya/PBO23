    private void cmbprodukActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbprodukActionPerformed
        // TODO add your handling code here:
        String produk;
        double hargasatuan;
        
        produk = String.valueOf(cmbproduk.getSelectedItem());
        
        if (produk.equals("Topi")){
            hargasatuan = 10000;
        } else if (produk.equals("Baju")){
            hargasatuan = 20000;
        } else if (produk.equals("Celana")){
            hargasatuan = 30000;
        } else {
            hargasatuan = 40000;
        }
        
        txthargasatuan.setText(""+hargasatuan);
    }//GEN-LAST:event_cmbprodukActionPerformed

